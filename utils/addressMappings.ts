// Fix: Added the standardizeRegion function to resolve import errors in `services/processing.worker.ts`.
export function standardizeRegion(regionString: string): string {
    if (!regionString) return 'Регион не определен';
    const normalized = regionString.toLowerCase().trim().replace(/[^a-zа-я\s-]/g, '');

    const sortedKeywords = Object.keys(REGION_KEYWORD_MAP).sort((a, b) => b.length - a.length);

    for (const keyword of sortedKeywords) {
        if (normalized.includes(keyword)) {
            return REGION_KEYWORD_MAP[keyword];
        }
    }
    
    for (const canonical of Object.values(REGION_KEYWORD_MAP)) {
        if (canonical.toLowerCase() === normalized) {
            return canonical;
        }
    }

    return 'Регион не определен';
}
